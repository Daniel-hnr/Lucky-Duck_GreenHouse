	MX_TIM2_Init();
	MX_RTC_Init();
	/* USER CODE BEGIN 2 */
	HAL_ADC_Start(&hadc1);
	ssd1306_Init();
	init_dht11(&dht, &htim2, DHT11_GPIO_Port, DHT11_Pin);
